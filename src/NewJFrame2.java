    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        listXModel.clear();
        listYModel.clear();
        for (int x = -8; x < -2; x++) {
            listXModel.addElement(x);
            listYModel.addElement(calcFunction(x));
        }
        for (int x =-2; x < 5; x++){
        listXModel.addElement(x);
            listYModel.addElement(calcFunction1(x));
        }
         for (int x =5; x < 9; x++){
        listXModel.addElement(x);
            listYModel.addElement(calcFunction2(x));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private double calcFunction(double x) {
         double result = (x*x+1)/(x-2);   
        return result;   
    }
    private double calcFunction1(double x) {
         double result = 2*x*x*x+4;   
        return result;   
    }
    private double calcFunction2(double x) {
        double k = Math.E ;
         double result = 5*x + Math.pow( k , x);   
        return result;   
    }
