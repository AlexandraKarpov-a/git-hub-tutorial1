    private void loadImage(JLabel label, String imgPath) {
        try {
            BufferedImage img = ImageIO.read(new File(imgPath));
            Image scaledImg = img.getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_DEFAULT);
            ImageIcon icon = new ImageIcon(scaledImg);
            label.setIcon(icon);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

